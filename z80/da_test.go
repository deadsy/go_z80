type memory struct {
	data []byte
}

const memPad = 8

func newMemory(data []byte) *memory {
	n := len(data) + memPad
	m := &memory{
		data: make([]byte, n),
	}
	for i := range data {
		m.data[i] = data[i]
	}
	return m
}

func (m *memory) Rd8(adr uint16) uint8 {
	return m.data[adr]
}

func (m *memory) Wr8(adr uint16, val uint8)   {}
func (m *memory) Wr16(adr uint16, val uint16) {}
func (m *memory) Rd16(adr uint16) uint16      { return 0 }

